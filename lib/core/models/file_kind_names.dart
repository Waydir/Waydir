  'license': 'License document',
  'copying': 'License document',
  'authors': 'Authors document',
  'changelog': 'Changelog document',
};

String kindLabelForFile(String name, String extension) {
  final fileName = name.toLowerCase();
  final fileNameKind = _fileNameKindNames[fileName];
  if (fileNameKind != null) return fileNameKind;

  final extensionKind = _kindNames[extension];
  if (extensionKind != null) return extensionKind;
  if (extension.isEmpty) return 'File';

  return '${extension.toUpperCase()} file';
}
