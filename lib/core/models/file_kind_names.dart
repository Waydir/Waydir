const _fileNameKindNames = <String, String>{
  'dockerfile': 'Dockerfile',
  'containerfile': 'Containerfile',
  'makefile': 'Makefile',
  'rakefile': 'Rakefile',
  'gemfile': 'Gemfile',
  'podfile': 'Podfile',
